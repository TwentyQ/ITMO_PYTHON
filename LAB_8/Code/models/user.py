class User:
    """
    Класс, представляющий пользователя системы.

    Атрибуты:
        __id (int): Приватное поле для хранения уникального идентификатора пользователя
        __name (str): Приватное поле для хранения имени пользователя
    """

    def __init__(self, id: int, name: str):
        """
        Инициализирует объект User с заданными параметрами.

        Аргументы:
            id: Уникальный идентификатор пользователя (должен быть положительным)
            name: Имя пользователя (должно быть не менее 2 символов)
        """
        # Приватное поле для хранения идентификатора пользователя
        self.__id: int = id

        # Приватное поле для хранения имени пользователя
        self.__name: str = name

    @property
    def id(self):
        """
        Геттер для получения идентификатора пользователя.

        Возвращает:
            int: Уникальный идентификатор пользователя
        """
        return self.__id

    @id.setter
    def id(self, id: int):
        """
        Сеттер для установки нового идентификатора пользователя с валидацией.

        Аргументы:
            id: Новый идентификатор пользователя

        Выбрасывает:
            ValueError: Если идентификатор не является целым числом или не положительный
        """
        # Проверяем, что id является целым числом и положительным
        if type(id) is int and id > 0:
            self.__id = id
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании id')

    @property
    def name(self):
        """
        Геттер для получения имени пользователя.

        Возвращает:
            str: Имя пользователя
        """
        return self.__name

    @name.setter
    def name(self, name: str):
        """
        Сеттер для установки нового имени пользователя с валидацией.

        Аргументы:
            name: Новое имя пользователя

        Выбрасывает:
            ValueError: Если имя не является строкой или короче 2 символов
        """
        # Проверяем, что name является строкой и имеет длину не менее 2 символов
        if type(name) is str and len(name) >= 2:
            self.__name = name
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании имени пользователя')
