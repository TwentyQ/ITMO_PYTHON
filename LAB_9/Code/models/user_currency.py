class UserCurrency:
    """
    Класс, представляющий связь между пользователем и валютой.

    Атрибуты:
        __id (int): Приватное поле для хранения уникального идентификатора связи
        __user_id (int): Приватное поле для хранения идентификатора пользователя
        __currency_id (int): Приватное поле для хранения идентификатора валюты
    """

    def __init__(self, id: int, user_id: int, currency_id: int) -> None:
        """
        Инициализирует объект UserCurrency с заданными параметрами.

        Аргументы:
            id: Уникальный идентификатор связи (должен быть неотрицательным)
            user_id: Идентификатор пользователя (должен быть неотрицательным)
            currency_id: Идентификатор валюты (должен быть неотрицательным)
        """
        # Инициализация приватных полей нулевыми значениями
        self.__id: int = 0
        self.__user_id: int = 0
        self.__currency_id: int = 0

        # Установка значений через сеттеры для валидации
        self.id = id
        self.user_id = user_id
        self.currency_id = currency_id

    @property
    def id(self):
        """
        Геттер для получения идентификатора связи.

        Возвращает:
            int: Уникальный идентификатор связи между пользователем и валютой
        """
        return self.__id

    @id.setter
    def id(self, id: int):
        """
        Сеттер для установки нового идентификатора связи с валидацией.

        Аргументы:
            id: Новый идентификатор связи

        Выбрасывает:
            ValueError: Если идентификатор не является целым числом или отрицательный
        """
        # Проверяем, что id является целым числом и неотрицательным
        if type(id) is int and id >= 0:
            self.__id = id
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании номера связи')

    @property
    def user_id(self):
        """
        Геттер для получения идентификатора пользователя.

        Возвращает:
            int: Идентификатор пользователя, связанного с валютой
        """
        return self.__user_id

    @user_id.setter
    def user_id(self, user_id: int) -> None:
        """
        Сеттер для установки нового идентификатора пользователя с валидацией.

        Аргументы:
            user_id: Новый идентификатор пользователя

        Выбрасывает:
            ValueError: Если идентификатор пользователя не является целым числом или отрицательный
        """
        # Проверяем, что user_id является целым числом и неотрицательным
        if type(user_id) is int and user_id >= 0:
            self.__user_id = user_id
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании id пользователя')

    @property
    def currency_id(self) -> int:
        """
        Геттер для получения идентификатора валюты.

        Возвращает:
            int: Идентификатор валюты, связанной с пользователем
        """
        return self.__currency_id

    @currency_id.setter
    def currency_id(self, currency_id: int) -> None:
        """
        Сеттер для установки нового идентификатора валюты с валидацией.

        Аргументы:
            currency_id: Новый идентификатор валюты

        Выбрасывает:
            ValueError: Если идентификатор валюты не является целым числом или отрицательный
        """
        # Проверяем, что currency_id является целым числом и неотрицательным
        if type(currency_id) is int and currency_id >= 0:
            self.__currency_id = currency_id
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании id валюты')