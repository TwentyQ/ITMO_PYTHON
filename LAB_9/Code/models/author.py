class Author:
    """
    Класс, представляющий автора приложения с его основными метаданными.

    Атрибуты:
        __name (str): Приватное поле для хранения имени автора
        __group (str): Приватное поле для хранения группы автора
    """

    def __init__(self, name: str, group: str) -> None:
        """
        Инициализирует объект Author с заданными параметрами.

        Аргументы:
            name: Имя автора (должно быть не менее 2 символов)
            group: Группа автора (должна быть не менее 6 символов)
        """
        # Приватное поле для хранения имени автора
        self.__name: str = name

        # Приватное поле для хранения группы автора
        self.__group: str = group

    @property
    def name(self) -> str:
        """
        Геттер для получения имени автора.

        Возвращает:
            str: Имя автора
        """
        return self.__name

    @name.setter
    def name(self, name: str) -> None:
        """
        Сеттер для установки имени автора с валидацией.

        Аргументы:
            name: Имя автора

        Выбрасывает:
            ValueError: Если имя не является строкой или короче 2 символов
        """
        # Проверяем, что name является строкой и имеет длину не менее 2 символов
        if type(name) is str and len(name) >= 2:
            self.__name = name
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании имени автора')

    @property
    def group(self) -> str:
        """
        Геттер для получения группы автора.

        Возвращает:
            str: Группа автора
        """
        return self.__group

    @group.setter
    def group(self, group: str) -> None:
        """
        Сеттер для установки группы автора с валидацией.

        Аргументы:
            group: Группа автора

        Выбрасывает:
            ValueError: Если группа не является строкой или короче 6 символов
        """
        # Проверяем, что group является строкой и имеет длину более 5 символов
        if type(group) is str and len(group) > 5:
            self.__group = group
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании группы автора')