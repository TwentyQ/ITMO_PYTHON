class Currency:
    """
    Класс, представляющий валюту с её основными характеристиками.

    Атрибуты:
        __id (int): Приватное поле для хранения уникального идентификатора валюты
        __num_code (int): Приватное поле для хранения цифрового кода валюты (ISO 4217)
        __char_code (str): Приватное поле для хранения символьного кода валюты (ISO 4217)
        __name (str): Приватное поле для хранения названия валюты
        __value (float): Приватное поле для хранения курса валюты
        __nominal (int): Приватное поле для хранения номинала валюты
    """

    def __init__(self, id: int, num_code: int, char_code: str,
                 name: str, value: float, nominal: int):
        """
        Инициализирует объект Currency с заданными параметрами.

        Аргументы:
            id: Уникальный идентификатор валюты (должен быть неотрицательным)
            num_code: Цифровой код валюты (1-999)
            char_code: Символьный код валют (3 символа)
            name: Название валюты
            value: Курс валюты (значение с плавающей точкой)
            nominal: Номинал валюты (должен быть положительным)
        """
        # Устанавливаем значения через сеттеры для валидации
        self.id = id
        self.num_code = num_code
        self.char_code = char_code
        self.name = name
        self.value = value
        self.nominal = nominal

    @property
    def id(self):
        """
        Геттер для получения идентификатора валюты.

        Возвращает:
            int: Уникальный идентификатор валюты
        """
        return self.__id

    @id.setter
    def id(self, id: int):
        """
        Сеттер для установки нового идентификатора валюты с валидацией.

        Аргументы:
            id: Новый идентификатор валюты

        Выбрасывает:
            ValueError: Если идентификатор не является целым числом или отрицательный
        """
        # Проверяем, что id является целым числом и неотрицательным
        if type(id) is int and id >= 0:
            self.__id = id
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании id валюты')

    @property
    def num_code(self):
        """
        Геттер для получения цифрового кода валюты.

        Возвращает:
            int: Цифровой код валюты
        """
        return self.__num_code

    @num_code.setter
    def num_code(self, num_code: int):
        """
        Сеттер для установки нового цифрового кода валюты с валидацией.

        Аргументы:
            num_code: Новый цифровой код валюты

        Выбрасывает:
            ValueError: Если код не является целым числом или вне диапазона 1-999
        """
        # Проверяем, что num_code является целым числом в диапазоне 1-999
        if type(num_code) is int and num_code >= 1 and num_code <= 999:
            self.__num_code = num_code
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании цифрового кода валюты')

    @property
    def char_code(self):
        """
        Геттер для получения символьного кода валюты.

        Возвращает:
            str: Символьный код валюты
        """
        return self.__char_code

    @char_code.setter
    def char_code(self, char_code: str):
        """
        Сеттер для установки нового символьного кода валюты с валидацией.

        Аргументы:
            char_code: Символьный код валюты

        Выбрасывает:
            ValueError: Если код не является строкой или имеет длину не 3 символа
        """
        # Проверяем, что char_code является строкой длиной ровно 3 символа
        if type(char_code) is str and len(char_code) == 3:
            self.__char_code = char_code
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании символьного кода валюты')

    @property
    def name(self):
        """
        Геттер для получения названия валюты.

        Возвращает:
            str: Название валюты
        """
        return self.__name

    @name.setter
    def name(self, name: str):
        """
        Сеттер для установки нового названия валюты с валидацией.

        Аргументы:
            name: Новое название валюты

        Выбрасывает:
            ValueError: Если название не является строкой
        """
        # Проверяем, что name является строкой
        if type(name) is str and len(name) >= 0:
            self.__name = name
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании имени валюты')

    @property
    def value(self):
        """
        Геттер для получения курса валюты.

        Возвращает:
            float: Курс валюты
        """
        return self.__value

    @value.setter
    def value(self, value: float):
        """
        Сеттер для установки нового курса валюты с валидацией.

        Аргументы:
            value: Новый курс валюты

        Выбрасывает:
            ValueError: Если курс не является числом с плавающей точкой
        """
        # Проверяем, что value является числом с плавающей точкой
        if type(value) is float:
            self.__value = value
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании курса валюты')

    @property
    def nominal(self):
        """
        Геттер для получения номинала валюты.

        Возвращает:
            int: Номинал валюты
        """
        return self.__nominal

    @nominal.setter
    def nominal(self, nominal: int):
        """
        Сеттер для установки нового номинала валюты с валидацией.

        Аргументы:
            nominal: Новый номинал валюты

        Выбрасывает:
            ValueError: Если номинал не является целым числом или не положительный
        """
        # Проверяем, что nominal является целым числом и положительным
        if type(nominal) is int and nominal >= 1:
            self.__nominal = nominal
        else:
            # Генерируем исключение, если данные невалидны
            raise ValueError('Ошибка при задании имени валюты')
